package assignments;

public class SwapNum {


		public static void main(String[] args) {

			int a=10;
			int b=20;
			int c;
			
			//swapping numbers
			
			c=a;
			a=b;
			b=c;
			
			System.out.println("a equals to " +a +" "+"after swapping");
			System.out.println("b equals to " +b +" " +"after swapping");
		}
	

}
